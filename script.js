function renderCards(targetId, items) {
  const container = document.getElementById(targetId);
  if (!container) return;

  container.innerHTML = items
    .map(
      (item) => `
      <article class="card">
        ${item.image ? `<img src="${item.image}" alt="${item.title}" class="card-image" />` : ""}
        <div class="card-content">
          <span class="meta">${item.meta}</span>
          <h3>${item.title}</h3>
          <p>${item.description}</p>
          <div class="card-links">
            ${item.linkUrl ? `<a href="${item.linkUrl}" target="_blank" rel="noopener">${item.linkLabel}</a>` : ""}
            ${item.secondaryLink ? `<a href="${item.secondaryLink.url}" target="_blank" rel="noopener" class="secondary-link">${item.secondaryLink.label}</a>` : ""}
          </div>
        </div>
      </article>
    `
    )
    .join("");
}

